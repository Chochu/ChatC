	protected void TabMouseClicked(MouseEvent evt) {
		// remove tab when double click
		if(evt.getClickCount() == 2){
			int index = Tab.getSelectedIndex(); //get index
			Tab.removeTabAt(index); //close tab at index
			//clientnet.LeaveChatRoom(getRoomID(Tab.getTitleAt(index))); //send to server to close tab with room name
		}
	}

	private void Room_JlistMouseClicked(java.awt.event.MouseEvent evt) {                                        
		// Open a new tab
		if(evt.getClickCount() == 2){
			int index = Room_Jlist.locationToIndex(evt.getPoint());
			
			ListModel dlm = Room_Jlist.getModel();
			Object item = dlm.getElementAt(index);;
			Room_Jlist.ensureIndexIsVisible(index);
			Tab.addTab(""+item, new ChatTabPane(clientnet,""+item));
			//clientnet.JoinChatRoom(getRoomID(item)); //send to server that the user has join the room name
		}
	}                                       
